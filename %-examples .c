// Integer
int a = 10;
printf("%d\n", a);  // prints: 10

// Long integer
long b = 1234567890L;
printf("%ld\n", b);  // prints: 1234567890

// Long long integer
long long c = 9876543210123456789LL;
printf("%lld\n", c);  // prints: 9876543210123456789

// Float
float f = 3.14f;
printf("%f\n", f);  // prints: 3.140000

// Double
double d = 3.1415926535;
printf("%lf\n", d);  // prints: 3.141593


long double d = 3.1415926535;
printf("%Lf\n", d);  // prints: 3.141593
